public class TaskInput implements java.io.Serializable
{
    private String name;
    private byte[] data;
    public TaskInput(String f, byte[] b)
    {
        name = f;
        data = b;
    }
    public void setName(String f)
    {
        name = f;
    }
    public String getName()
    {
        return name;
    }
    public byte[] getData()
    {
        return data;
    }
    public void setData(byte[] d)
    {
        data = d;
    }
}
