public class Status
{
    private static String status;
    private static String last_error="N/A";
    public static void set(String s)
    {
        status = s;
    }
    public static String get()
    {
        return status;
    }
    public static void setLastError(String err)
    {
        last_error = err;
    }
    public static String getLastError()
    {
        return last_error;
    }
}
